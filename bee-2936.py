curupira = int(input())
boitata = int(input())
boto = int(input())
mapinguari = int(input())
iara = int(input())

gramas_curupira = 300
gramas_boitata = 1500
gramas_boto = 600
gramas_mapinguari = 1000
gramas_iara = 150

soma = 225 + (curupira * gramas_curupira) + (boitata * gramas_boitata) + (boto * gramas_boto) + (mapinguari * gramas_mapinguari) + (iara * gramas_iara)
print(soma)
